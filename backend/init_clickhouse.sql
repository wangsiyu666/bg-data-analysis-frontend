-- 创建运营用户清单表
CREATE TABLE IF NOT EXISTS user_strategies (
    month_num String COMMENT '运营月份，格式 YYYYMM（例如 202604）',
    user_id String COMMENT '用户唯一标识',
    city_id String COMMENT '城市ID',
    yy_date String COMMENT '运营时间，格式 YYYYMMDDHH24（例如 202604161430）',
    strategy_id String COMMENT '策略ID（关联 strategies 表）',
    product_id String COMMENT '产品ID（关联 products 表）',
    plan_id String COMMENT '执行计划ID（关联 execution_plans 表）',
    script_id String COMMENT '话术ID（关联 scripts 表）',
    is_yy UInt8 COMMENT ' 是否运营（0/1）',
    is_jt UInt8 COMMENT '是否接通（0/1）',
    jt_date String COMMENT '接通时间，格式 YYYYMMDDHH24',
    is_zh UInt8 COMMENT '是否转化（0/1）',
    roi Float64 COMMENT '投资回报率',
    zh_date String COMMENT '转化时间，格式 YYYYMMDDHH24',
    extra_fields String  COMMENT '备用字段（JSON 格式）',
) ENGINE = MergeTree
ORDER BY (month_num)
COMMENT '运营用户清单表';

-- 创建前端客群保存清单
CREATE TABLE audience_groups (
    audience_id String,      -- 客群唯一标识（如 "aud_1234567890"）
    name String,             -- 客群名称
    condition_sql String,    -- 原始条件SQL（便于追溯）
    user_id String,          -- 用户ID（每个用户一行）
    created_at DateTime      -- 创建时间
) ENGINE = MergeTree ORDER BY (audience_id, user_id)

